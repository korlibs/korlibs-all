object OpenglDesc {
	data class FunctionName(val name: String, val jsName: String = name, val nativeName: String = name)
	data class Constant(val name: String, val value: Int) {
		val unprefixedName = name.removePrefix("GL_")
	}

	data class Argument(val name: String, val type: GlType)
	data class Function(
		val fname: FunctionName,
		val rettype: GlType,
		val args: List<Argument>,
		val jsBody: String? = null,
		val androidBody: String? = null,
		val nativeBody: String? = null,
		val jvmBody: String? = null,
		val core: Boolean = false
	) {
		val name = fname.name
		val unprefixedName = name.removePrefix("gl").decapitalize()
		val jsName get() = fname.jsName.removePrefix("gl").decapitalize()
	}

	val constants = LinkedHashMap<String, Constant>()
	val functions = ArrayList<Function>()

	fun constant(name: String, value: Int) {
		constants[name] = Constant(name, value)
	}

	fun Arguments(vararg args: Pair<String, GlType>) = args.map { Argument(it.first, it.second) }

	fun add(func: Function) {
		functions.add(func)
	}

	//@Deprecated(replaceWith = ReplaceWith("add(Function(name, rettype, Arguments(*args), jsBody = jsBody, androidBody = androidBody, nativeBody = nativeBody, jvmBody = jvmBody, core = core))"))
	fun function(
		rettype: GlType,
		name: FunctionName,
		vararg args: Pair<String, GlType>,
		jsBody: String? = null,
		androidBody: String? = null,
		nativeBody: String? = null,
		jvmBody: String? = null,
		core: Boolean = false
	) {
		functions.add(
			Function(
				name,
				rettype,
				args.map { Argument(it.first, it.second) }.toList(),
				jsBody = jsBody,
				androidBody = androidBody,
				nativeBody = nativeBody,
				jvmBody = jvmBody,
				core = core
			)
		)
	}

	open class GlType(val ktname: String) {
		//open val ktnameJS: String get() = ktname
		open fun toJVM(param: String): String = param

		open fun toAndroid(param: String): String = toJVM(param)
		open fun toJSParam(param: String): String = param
		open fun toNative(param: String): String = param
		open fun toNativeIphone(param: String): String = toNative(param)
		open fun toNativeWin32(param: String): String = toNative(param)
		fun toNative(param: String, platform: GenGl.NativeTarget) = when (platform) {
			GenGl.NativeTarget.IPHONE -> toNativeIphone(param)
			GenGl.NativeTarget.WIN32 -> toNativeWin32(param)
			else -> toNative(param)
		}

		open fun toJSReturn(param: String): String = param
		open fun toNativeReturn(param: String): String = param
	}

