	fun Printer.generateNative(target: NativeTarget) {
		println("// WARNING: File autogenerated DO NOT modify")
		println("// https://www.khronos.org/registry/OpenGL/api/GLES2/gl2.h")
		fileSupress()
		println("")
		println("package com.soywiz.kgl")
		println("")
		println("import com.soywiz.kmem.*")
		println("import kotlinx.cinterop.*")
		println("import com.soywiz.korim.bitmap.*")
		println("import com.soywiz.korim.format.*")
		//println("import platform.GLUT.*")
		//OpenGLES2
		when (target) {
			NativeTarget.MACOS -> {
				println("import platform.OpenGL.*")
				println("import platform.OpenGLCommon.*")
			}
			NativeTarget.IPHONE -> {
				println("import platform.gles2.*")
			}
			NativeTarget.WIN32 -> {
				println("import platform.opengl32.*")
				println("import platform.windows.*")
			}
		}
		println("import platform.posix.*")
		println("")
		println("class KmlGlNative : KmlGl() {")
		println("    val tempBufferAddress = TempBufferAddress()")
		for (func in OpenglDesc.functions) {
			val nativeName = when (target) {
				NativeTarget.IPHONE -> func.name
				NativeTarget.WIN32 -> func.fname.nativeName
				else -> func.fname.nativeName
			}
			val call2 = func.nativeBody
					?: func.rettype.toNativeReturn("$nativeName(${func.args.joinToString(", ") {
						it.type.toNative(
							it.name,
							target
						)
					}})")

			val call = when (func.rettype) {
				OpenglDesc.GlVoid, OpenglDesc.GlString, OpenglDesc.GlBool -> call2
				else ->"$call2.convert()"
			}
			val hasStrings = func.args.any { it.type == OpenglDesc.GlString }
			val funcDecl =
				"override fun ${func.unprefixedName}(${func.args.joinToString(", ") { it.name + ": " + it.type.ktname }}): ${func.rettype.ktname}"
			if (hasStrings) {
				println("    $funcDecl = memScoped { tempBufferAddress { $call } }")
			} else {
				println("    $funcDecl = tempBufferAddress { $call }")
			}
		}

		if (target == NativeTarget.WIN32) {
			//println("val OPENGL32_DLL_MODULE: HMODULE? by lazy { LoadLibraryA(\"opengl32.dll\") }")
			//println("fun wglGetProcAddressAny(name: String): PROC? = wglGetProcAddress(name) ?: GetProcAddress(OPENGL32_DLL_MODULE, name)")
			//println("val String.glstr: CPointer<GLcharVar> get() = this.cstr.uncheckedCast()")
			println("")
			println("    companion object {")
			for (func in OpenglDesc.functions) {
				val name = func.fname.nativeName
				val PROC = "PFN${name.toUpperCase()}PROC"
				if (!func.core) {
					println("        val $name: $PROC by lazy { wglGetProcAddressAny(\"$name\").uncheckedCast<$PROC>() }")
				}
			}
			println("    }")
		}

		println("}")

		println("")
	}

